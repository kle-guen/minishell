#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	x;
	char	c;

	x = 0;
	if (s != NULL)
	{
		while (s[x] != '\0' && fd != '\0')
		{
			c = s[x];
			write(fd, &c, 1);
			x++;
		}
	}
}
