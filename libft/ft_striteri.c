#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	x;

	x = 0;
	if (s != NULL)
	{
		while (s[x] != '\0')
		{
			f(x, &s[x]);
			x++;
		}
	}
}
